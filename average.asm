data segment
    numbers dw 1234, 5678, 9012, 3456, 7890 ; Масив 16-бітних чисел
    count   dw 5                                 ; Кількість чисел у масиві
    result  dw 0                                 ; Змінна для зберігання результату
data ends

; Код програми
code segment
    assume cs:code, ds:data

    start:
    mov ax, data
    mov ds, ax ; Встановлюємо сегмент даних

    xor dx, dx ; Очищаємо старший регістр DX
    mov ax, 0  ; Очищаємо регістр AX для накопичення суми

    mov cx, [count] ; Завантажуємо кількість чисел у лічильник CX
    mov bx, 0       ; Використовуємо BX для індексації масиву

    sum_loop:
    add ax, [numbers+bx] ; Додаємо наступне число до AX
    adc dx, 0            ; Якщо було переповнення, інкрементуємо DX
    add bx, 2            ; Переходимо до наступного елемента масиву (16-бітні числа)
    loop sum_loop        ; Повторюємо цикл для всіх чисел

mov bx, [count] ; Завантажуємо кількість чисел у BX
    div bx          ; Ділимо суму в DX:AX на кількість чисел у BX
                    ; Результат міститься в AX

    mov [result], ax ; Зберігаємо результат у змінній result

    mov ah, 4Ch ; Системний виклик для завершення програми
    int 21h

code ends
end start